#include "Person.h"
#include <string>
#include <iostream>
using namespace std;
//конструктор без параметров
Person::Person(void)
{
    name = "";
    age = 0;
}
//деструктор
Person::~Person(void)
{
}
//констрктор с параметрами
Person::Person(string M, int C)
{
    name = M;
    age = C;
}
//конструктор копирования
Person::Person(const Person& car)
{
    name = car.name;
    age = car.age;
}
//селекторы
void Person::Set_age(int C)
{
    age = C;
}
void Person::Set_name(string M)
{
    name = M;
}
//оператор присваивания
Person& Person::operator=(const Person& c)
{
    if (&c == this)return *this;
    name = c.name;
    age = c.age;
    return *this;
}
//метод для просмотра атрибутов
void Person::Show()
{
    cout << "\nNAME : " << name;
    cout << "\nAGE : " << age;
    cout << "\n";
}
//метод для ввода значений атрибутов
void Person::Input()
{
    cout << "\nname:"; cin >> name;
    cout << "\nage:";cin >> age;
}
